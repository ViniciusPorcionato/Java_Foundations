public class ExerciseIfElse {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        //Variavel do tipo int
        int k = 15;
        if (k > 20) {
            System.out.println("O valor de k é maior que 20");
        } else if (k > 10) {
            System.out.println("O valor de k é maior que 10");
        } else {
            System.out.println("O valor de k é menor que 10");
        }

    }

}
